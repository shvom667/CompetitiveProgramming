void solve() {
  ll n, k;
  string s;
  cin >> n >> k >> s;
  Vec<ll> ind;
  for (ll i = 0; i < n; i++) {
    ind.pb(i);
  }
  Vec<ll> I, U;
  I.pb(n);
  while (true) {
    ll lci = I.back();
    auto it = lower_bound(begin(ind),end(ind),lci-1-k);
    if (!(it == end(ind)) && *it < lci+k) {
      U.pb(*it);
      I.pb(*it-k);
    }
    else {
      if (it == begin(ind)) break;
      auto kt = it;
      --kt;
      U.pb(*kt);
      I.pb(*kt-k);
    }
  }
  for (auto&x : U) {
    cout << x << " ";
  } cout << '\n';
}
