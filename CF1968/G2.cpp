vector<int> z_function(string s) {
    int n = s.size();
    vector<int> z(n);
    int l = 0, r = 0;
    for(int i = 1; i < n; i++) {
        if(i < r) {
            z[i] = min(r - i, z[i - l]);
        }
        while(i + z[i] < n && s[z[i]] == s[i + z[i]]) {
            z[i]++;
        }
        if(i + z[i] > r) {
            l = i;
            r = i + z[i];
        }
    }
    return z;
}
void solve() {
    ll n, l, r;
    cin >> n >> l >> r;
    string s;
    cin >> s;
    auto z = z_function(s);
    z[0] = n;
    dbg(z);
    Vec<ll> iz[n+2];
    for (ll i = 0; i < n; i++) {
        iz[z[i]].push_back(i);
    }
    Vec<ll> res(n + 1, 0);
    set<ll> ind;
    for (ll i = n; i >= 1; i--) {
        for (auto& x : iz[i]) {
            ind.insert(x);
        }
        dbg(ind);
        ll fans = 0;
        if (ind.empty())continue;
        auto cur = *begin(ind);
        fans++;
        while (true) {
            auto it = ind.upper_bound(cur+i-1);
            if (it == end(ind))break;
            cur = *it;
            fans++;
        }
        res[i]=fans;
    }
    Vec<ll> ans(n+1, 0);
    for (ll i = 1; i <= n; i++) {
        ans[res[i]] = max(ans[res[i]], i);
    }
    for (ll i = n - 1; i >= 1; i--)
        ans[i] = max(ans[i], ans[i + 1]);
    dbg(res);
    dbg(ans);
    for (ll i = l; i <= r; i++)
        cout << ans[i] << " ";
    cout << '\n';
}
