void solve() {
  ll n, z;
  cin >> n >> z;
  Vec<ll> x(n);
  for (ll i = 0; i < n; i++)
    cin >> x[i];
  sort(begin(x), end(x));
  auto cond = [&] (ll mid) {
    multiset<ll> st;
    Vec<ll> en;
    for (ll c = 1; c <= n; c++) {
      if (c <= mid) {
        en.pb(x[n-c]);
      } else {
        st.insert(x[n-c]);
      }
    } 
    dbg(st);
    dbg(en);
    sort(begin(en), end(en));
    for (auto& v : en) {
      auto it = st.upper_bound(v-z);
      if (it == begin(st)) {
        return false;
      }
      it--;
      st.erase(it);
    }

    return true;
  };
  ll l = 0, r = n / 2;
  while (l <= r) {
    ll mid = l + (r - l) / 2;
    if (cond(mid)) {
      l = mid + 1;
    } else {
      r = mid - 1;
    }
  }
  cout << r << "\n";
}
